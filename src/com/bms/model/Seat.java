package com.bms.model;

public class Seat {

	private int row,rol;

	public Seat(int row, int rol) {
		super();
		this.row = row;
		this.rol = rol;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getRol() {
		return rol;
	}

	public void setRol(int rol) {
		this.rol = rol;
	}
	
	
	
}
